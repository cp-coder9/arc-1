// Property 18: Copyrighted Text Limit
// Feature: ai-copilot-workspace, Property 18: Copyrighted Text Limit
// Validates: Requirements 10.4
// ══════════════════════════════════════════════════════════════════════════════

describe('Feature: ai-copilot-workspace, Property 18: Copyrighted Text Limit', () => {
  /**
   * **Validates: Requirements 10.4**
   *
   * For any clause explanation output, no sequence of more than 15 consecutive words
   * from copyrighted contract forms should appear. checkCopyrightCompliance() must
   * flag violations.
   */

  it('flags content with legal phrases exceeding 15 consecutive words', () => {
    fc.assert(
      fc.property(
        // Generate legal-sounding contract phrases that trigger pattern matching
        fc.constantFrom(
          'the contractor shall be liable',
          'the employer shall not be responsible',
          'notwithstanding anything contained in this',
          'subject to the provisions of clause',
          'without prejudice to the generality of the',
          'the principal agent shall issue',
          'practical completion shall be deemed to have been achieved',
        ),
        // Add enough trailing words to exceed 15 consecutive
        fc.array(
          fc.constantFrom(
            'and',
            'the',
            'provisions',
            'of',
            'this',
            'contract',
            'shall',
            'apply',
            'to',
            'all',
            'parties',
            'hereto',
            'binding',
            'upon',
            'execution',
          ),
          { minLength: 12, maxLength: 20 },
        ),
        (legalPhrase, trailingWords) => {
          const content = legalPhrase + ' ' + trailingWords.join(' ');
          // Count total words from match point
          const words = content.trim().split(/\s+/);
          fc.pre(words.length > 15);

          const result = checkCopyrightCompliance(content);
          expect(result.compliant).toBe(false);
          expect(result.violations.length).toBeGreaterThan(0);
        },
      ),
      { numRuns: 100 },
    );
  });

  it('allows content with legal phrases of 15 or fewer consecutive words', () => {
    fc.assert(
      fc.property(
        fc.constantFrom(
          'the contractor shall be liable for damages',
          'the employer shall pay within 30 days',
          'subject to the provisions of clause 12',
          'without prejudice to the generality',
        ),
        (content) => {
          // These short legal phrases are within the 15-word limit
          const words = content.trim().split(/\s+/);
          fc.pre(words.length <= 15);

          const result = checkCopyrightCompliance(content);
          expect(result.compliant).toBe(true);
          expect(result.violations.length).toBe(0);
        },
      ),
      { numRuns: 100 },
    );
  });

  it('passes non-legal content regardless of length', () => {
    fc.assert(
      fc.property(
        // Generate generic text that doesn't match legal patterns
        fc.array(
          fc.constantFrom(
            'The project is progressing well',
            'Team members are on schedule',
            'Budget allocation remains within limits',
            'Design review completed successfully',
            'Client meeting scheduled for next week',
            'All deliverables are on track',
          ),
          { minLength: 1, maxLength: 5 },
        ),
        (sentences) => {
          const content = sentences.join('. ') + '.';
          const result = checkCopyrightCompliance(content);
          expect(result.compliant).toBe(true);
          expect(result.violations.length).toBe(0);
        },
      ),
      { numRuns: 100 },
    );
  });

  it('violations array contains the offending word sequences', () => {
    fc.assert(
      fc.property(
        fc.constantFrom(
          'the contractor shall be liable',
          'the employer shall not be responsible',
          'notwithstanding anything contained in this',
        ),
        fc.array(
          fc.constantFrom(
            'for',
            'all',
            'damages',
            'arising',
            'from',
            'any',
            'breach',
            'of',
            'the',
            'terms',
            'and',
            'conditions',
            'of',
            'this',
            'agreement',
          ),
          { minLength: 12, maxLength: 20 },
        ),
        (legalPhrase, trailingWords) => {
          const content = legalPhrase + ' ' + trailingWords.join(' ');
          const words = content.trim().split(/\s+/);
          fc.pre(words.length > 15);

          const result = checkCopyrightCompliance(content);
          if (!result.compliant) {
            for (const violation of result.violations) {
              const violationWords = violation.trim().split(/\s+/);
              // Violations should contain sequences of words
              expect(violationWords.length).toBeGreaterThan(0);
            }
          }
        },
      ),
      { numRuns: 100 },
    );
  });
});
