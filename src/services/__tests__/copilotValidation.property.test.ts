// Property 17: Disclaimer Invariant
// Feature: ai-copilot-workspace, Property 17: Disclaimer Invariant
// Validates: Requirements 12.3
// ══════════════════════════════════════════════════════════════════════════════

const DISCLAIMER = '\n\nAI-generated content. Review before professional use.';

describe('Feature: ai-copilot-workspace, Property 17: Disclaimer Invariant', () => {
  /**
   * **Validates: Requirements 12.3**
   *
   * For any response processed by the Copilot guardrails, the disclaimer
   * "AI-generated content. Review before professional use." must be appended.
   */

  it('appendDisclaimer always appends the disclaimer to any string', () => {
    fc.assert(
      fc.property(
        fc.string({ minLength: 0, maxLength: 5000 }),
        (content) => {
          const result = appendDisclaimer(content);
          expect(result.endsWith('AI-generated content. Review before professional use.')).toBe(
            true,
          );
        },
      ),
      { numRuns: 100 },
    );
  });

  it('appendDisclaimer preserves original content as a prefix', () => {
    fc.assert(
      fc.property(
        fc.string({ minLength: 0, maxLength: 5000 }),
        (content) => {
          const result = appendDisclaimer(content);
          expect(result).toBe(content + DISCLAIMER);
        },
      ),
      { numRuns: 100 },
    );
  });

  it('applyGuardrails always produces output ending with the disclaimer', () => {
    fc.assert(
      fc.property(
        // Generate safe content (no profanity, no PII) to test guardrails pipeline
        fc.array(fc.integer({ min: 65, max: 90 }), { minLength: 5, maxLength: 200 }),
        (codes) => {
          const content = codes.map((c) => String.fromCharCode(c)).join(' ');
          const result = applyGuardrails(content);
          expect(
            result.content.endsWith('AI-generated content. Review before professional use.'),
          ).toBe(true);
        },
      ),
      { numRuns: 100 },
    );
  });

  it('applyGuardrails appends disclaimer even after truncation', () => {
    fc.assert(
      fc.property(
        // Generate long safe content that will trigger truncation
        fc.array(fc.integer({ min: 65, max: 90 }), { minLength: 100, maxLength: 300 }),
        fc.integer({ min: 7800, max: 12000 }),
        (codes, extraLength) => {
          const base = codes.map((c) => String.fromCharCode(c)).join(' ');
          const content = base + ' word'.repeat(Math.ceil((extraLength - base.length) / 5));
          fc.pre(content.length > MAX_LENGTH);

          const result = applyGuardrails(content);
          expect(
            result.content.endsWith('AI-generated content. Review before professional use.'),
          ).toBe(true);
        },
      ),
      { numRuns: 100 },
    );
  });
});

